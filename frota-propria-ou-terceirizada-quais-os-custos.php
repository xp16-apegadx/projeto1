        <h1 class="post-title text-center">Frota própria ou terceirizada: quais os custos?</h1>
        <img src="http://blogdofrete.com.br/img/frota-propria-ou-tercerizada-quais-os-custos.jpg" class="panorama" title="Frota própria ou terceirizada: quais os custos?" alt="custos com frota" />
        
        <h2>Tem <strong>frota própria</strong> e está pensando em terceirizar? <strong>Terceiriza o serviço de transportes</strong> e está pensando em investir em frota própria? Vamos apresentar as vantagens e desvantagens das duas modalidades, vem com a gente!</h2>

        <p>Um estudo realizado pelo <a href="https://www.coppead.ufrj.br/pt-br/coppead/na-midia/60/#lpp" target="_blank">CELIG/COPPEAD</a>, aponta que 91% das grandes empresas brasileiras optaram pela terceirização do serviço de transporte, todavia, embora essa seja uma forte tendência, isso não significa que essa modalidade seja a ideal para o seu modelo de negócio.</p>

        <p>Para uma efetiva redução dos custos de logística é necessária uma avaliação caso a caso, considerando diversos fatores, como por exemplo: armazenamento do produto, rotas de distribuição, tipos de carga, tempo de entrega, cuidados com a carga e experiência do usuário.</p>

        <h2><strong>Frota própria</strong></h2>

          <p>É importante ressaltar, antes de qualquer outra coisa, que não basta apenas adquirir uma frota própria de carros ou caminhões, é preciso mantê-la. Essa manutenção envolve a compra de peças para reposição, reparos periódicos de danos causados pela condição das estradas, ou seja, é importante investir em uma oficina própria ou terceirizada.</p>
          
          <p>Ressaltamos ainda, que existirão custos com motoristas e demais funcionários para o controle de carga, descarga, gerenciamento de rotas, entre outras funções. Quanto maior a frota, maior a demanda de manutenção e a necessidade de níveis gerenciais, gerando a necessidade de mais capital humano.</p>

          <p>Deve constar ainda, na contabilização dos custos da frota própria, as despesas com seguro e eventuais multas.</p>

          <p>Mas nem só de gastos a frota própria é formada, temos algumas vantagens, como por exemplo: publicidade gratuita, com veículos circulando com a marca da sua empresa, flexibilidade de prazos e horários, relacionamento estreito com o cliente, e melhor tratamento do seu produto.</p>

        <h2><strong>Transporte terceirizado</strong></h2>

          <p>Ao terceirizar seu serviço de transporte, sua empresa vai eliminar diversas responsabilidades, diminuindo questões burocráticas que devem obrigatoriamente respeitadas, como por exemplo a contratação de funcionários e a observação das regras que regem essa relação de trabalho.</p>

          <p>Com a terceirização do sistema de transporte, sua empresa poderá investir tempo e capital humano em outros setores, poupando recursos e despesas operacionais.</p>

          <p>Porém, fugir dessa preocupação tem um custo. Ao terceirizar o serviço, sua empresa não terá a circulação da sua marca através dos veículos de transporte, devendo investir em outros meios de publicidade. Não há garantia do estado de conservação dos veículos utilizados pela empresa terceirizada, o que de certa forma, coloca a reputação da sua marca nas mãos de terceiros.</p>

        <h2><strong>E agora? Como decidir?</strong></h2>

          <p>Antes de tomar essa decisão, avalie a seguinte questão: você quer manter o controle sobre as suas operações de transporte ou confiar em um parceiro externo para atender essa demanda?</p>

          <p>Avalie com calma os prós e contras e as necessidades de logística da sua empresa, verificando o que funciona melhor com o seu modelo de negócio.</p>

