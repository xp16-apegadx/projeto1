        <h1 class="post-title text-center">Você sabe quais documentos fiscais são importantes para o transporte de cargas?</h1>
        <img src="http://blogdofrete.com.br/img/voce-sabe-quais-documentos-fiscais-sao-importantes-para-o-transporte-de-cargas.jpg" class="panorama" title="Você sabe quais documentos fiscais são importantes para o transporte de cargas?" alt="documenos fiscais importantes" />
        
        <p>Existem muitos <strong>documentos</strong> que são importantes na hora de <strong>transportar uma carga</strong> de um lugar para outro, <strong>você sabe quais documentos são esses</strong>?</p>

        <p>Preparamos esse post com os <strong>seis documentos fiscais</strong> indispensáveis para o transporte de mercadorias. Boa leitura!</p>

        <h2><strong>1. NOTA FISCAL ELETRÔNICA (NF-E)</strong></h2>
            
          <p>O primeiro documento que iremos falar é a Nota Fiscal Eletrônica que é emitido pelo dono da mercadoria (embarcador).</p>

          <p>Sua função é protocolar a venda de mercadorias, categorizando quem é o comprador e o vendedor do produto em transporte.</p>

          <p>Para que você possa transportar a mercadoria somente com nota fiscal você deve ser o emitente ou o destinatário da mercadoria.</p>

        <h2>DOCUMENTO DIGITAL/FÍSICO</h2>

          <p>A NF-e é realizada e gerada somente digitalmente através da Receita Federal o que facilita na hora de enviar através de e-mails para outros lugares, poupando tempo e agilizando no recebimento das informações.</p>

            <img src="http://blogdofrete.com.br/img/documento-digital-fisico.jpg" class="panorama" title="Documento Digital/Físico" alt="documenos fiscais importantes" />

          <p>Assim como, a nota fiscal tem a sua versão digital ela também possui a versão física, o DANFE (Documento Auxiliar da Nota Fiscal Eletrônica) este documento é de uso obrigatório e deve estar junto da carga desde o ponto de partida até o seu destino.</p>

          <p>Também é utilizado no momento de coletar assinatura de quem está recebendo a carga transportada junto com o registro do horário que aconteceu a entrega.</p>

        <h2><strong>2. CONHECIMENTO DE TRANSPORTE ELETRÔNICO (CT-E)</strong></h2>

          <p>O CT-e tem como finalidade comprovar o fornecimento de transporte de cargas. É feito eletronicamente para qualquer tipo de transporte. Quem tiver a necessidade de emitir um CT-e primeiro irá precisar da autorização e o cadastro na SEFAZ (Secretaria da Fazenda).</p>

          <h3><strong>PRA QUE SERVE?</strong></h3>

          <p>Esse documento também é usado na entrega da mercadoria, para que a pessoa que está recebendo assine o documento e conclua a prestação do serviço em questão.</p>
          
          <p>Caso o pedido não esteja com a quantidade correta ou com danos, a carga deve retornar com o DACTE (Documento Auxiliar do Conhecimento de Transporte Eletrônico).</p>

          <h3><strong>DOCUMENTO FÍSICO</strong></h3>

          <p>Assim como, o NF-e possuía o seu documento de auxílio, o CT-e também possui o DACTE. Este é usado para conferir dados no site da Secretaria da Fazenda e em fiscalizações.</p>

        <h2><strong>3. MANIFESTO ELETRÔNICO DE DOCUMENTOS FISCAIS</strong></h2>
          <p>(MDF-E)<br>
          Com a intenção de diminuir a necessidade de possuir vários documentos, foi criado pelo governo o MDF-e.</p>

          <p>Cuja a função, é ser usado em momentos em que o caminhão está transportando mercadorias diferentes e que necessitam de diferentes documentos fiscais, e de  diferentes CT-e’s dando para cada mercadoria uma identificação apropriada.</p>

          <h3><strong>VANTAGENS</strong></h3>

          <p>Outro benefício do MDF-e é que reúne todas as informações necessárias em um lugar só, é válido para todo o Brasil e com ele é possível reconhecer os detalhes da carga e do transporte.</p>

          <h3><strong>DOCUMENTO FÍSICO</strong></h3>

          <p>O MDF-e assim como, os outros documentos fiscais citados acima, possui o seu documento de apoio o Documento Auxiliar do Manifesto Eletrônico de Documentos Fiscais (DAMDFE) sua cópia física também para fins de fiscalização.</p>

        <h2><strong>4. NOTA FISCAL DE SERVIÇOS ELETRÔNICA (NFS-e)</strong></h2>

          <p>O NFS-e tem como função de catalogar os serviços oferecidos. O seu propósito é confirmar o recolhimento de impostos sobre qualquer mercadoria (ISSQN ou ISS).</p>
          
          <p>O valor sobre o imposto oscila entre 2% e 5% de acordo com a lei de cada cidade, portanto, o modelo de declaração também pode mudar conforme a cidade.</p>

          <p>A Nota Fiscal de Serviços Eletrônica é usada para transportes e na área da logística para registro e cobrar os transportes realizados dentro das cidades.</p>

        <h2><strong>5. CARTA DE CORREÇÃO ELETRÔNICA (CC-E)</strong></h2>

          <p>A carta de correção eletrônica como diz no nome tem como propósito de corrigir falhas em documentos como a nota fiscal eletrônica e o conhecimento de transporte eletrônico.</p>

          <h3><strong>O QUE CORRIGIR?</strong></h3>

          <p>Os erros que necessitam de correção, estes não devem ter relação com a mudança do destinatário, endereço, a data que foi feito a nota fiscal eletrônica e outros.</p>

          <p>O CC-E não é obrigatória em todos os estados do país, portanto é importante que entre no site da Secretaria da Fazenda e verifique a necessidade do uso da carta de correção, e alguns lugares o modo digital do documento não é usado, logo o impresso deve ser sempre utilizado.</p>

        <h2><strong>6.DECLARAÇÃO DE MERCADORIA</strong></h2>

          <p>Declaração destina-se apenas a acompanhar carga que não se destina a comercialização, como por exemplo, mudanças, bagagem não acompanhada, artigos pessoais,etc.</p>

          <p>Em caso algum deverá ser utilizada para mercadoria que se destina a comercializar, nem para a sua devolução.</p>

